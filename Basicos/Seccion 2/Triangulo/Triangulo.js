/*Cree un programa que tome la base y la altura de un triángulo e imprima su área.*/

let base = parseFloat(prompt("Digite el valor de la base de su triangulo para saber su area"));
let altura = parseFloat(prompt("Digite el valor de la altura de su triangulo para saber su area"));
let area = base*altura/2;
console.log("El area de su triangulo es",area);